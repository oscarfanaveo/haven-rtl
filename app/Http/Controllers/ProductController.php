    /**
     * Store a newly created resource in storage.
     */
    public function store(Request $request)
    {
        // Validación de los datos del formulario
        $validatedData = $request->validate([
            'nombre' => 'required|string|max:100',
            'categoria' => 'required|string|max:100',
            'precio' => 'required|numeric|min:0',
            'stock' => 'required|integer|min:0',
            'descripcion' => 'nullable|string', // nullable significa que no es obligatorio
        ]);

        // Crear el producto usando el modelo
        // El campo 'estado' tomará su valor por defecto 'TRUE'
        Producto::create($validatedData);

        // Redirigir a la página anterior con un mensaje de éxito
        return back()->with('success', '¡Producto añadido con éxito!');
